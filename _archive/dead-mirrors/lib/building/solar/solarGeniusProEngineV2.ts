export interface HourlyProduction {
  hour: number;
  irradiance: number;
  temperature: number;
  dcPower: number;
  acPower: number;
  efficiency: number;
  losses: {
    temperature: number;
    soiling: number;
    shading: number;
    mismatch: number;
    wiring: number;
    inverter: number;
  };
}

export function simulateHourlyProduction(
  systemSize: number,
  latitude: number,
  dayOfYear: number,
  panelTempCoef: number = -0.35,
  inverterEfficiency: number = 97.5
): HourlyProduction[] {
  const results: HourlyProduction[] = [];

  for (let hour = 5; hour <= 19; hour++) {
    // Solar position calculation
    const declination = 23.45 * Math.sin((2 * Math.PI / 365) * (dayOfYear - 81));
    const hourAngle = 15 * (hour - 12);
    const altitude = Math.asin(
      Math.sin(latitude * Math.PI / 180) * Math.sin(declination * Math.PI / 180) +
      Math.cos(latitude * Math.PI / 180) * Math.cos(declination * Math.PI / 180) *
      Math.cos(hourAngle * Math.PI / 180)
    );

    // Irradiance model (simplified clear sky)
    const irradiance = altitude > 0 ? Math.max(0, 1000 * Math.sin(altitude) * 0.9) : 0;

    // Temperature model
    const ambientTemp = 25 + 10 * Math.sin((hour - 6) * Math.PI / 12);
    const cellTemp = ambientTemp + irradiance * 0.03;

    // Temperature loss
    const tempLoss = Math.max(0, (cellTemp - 25) * Math.abs(panelTempCoef) / 100);

    // Other losses
    const soilingLoss = 0.02;
    const shadingLoss = hour < 8 || hour > 17 ? 0.05 : 0.01;
    const mismatchLoss = 0.02;
    const wiringLoss = 0.02;
    const inverterLoss = (100 - inverterEfficiency) / 100;

    // Power calculation
    const dcPower = systemSize * (irradiance / 1000) * (1 - tempLoss - soilingLoss - shadingLoss - mismatchLoss);
    const acPower = dcPower * (1 - wiringLoss - inverterLoss);

    results.push({
      hour,
      irradiance: Math.round(irradiance),
      temperature: Math.round(cellTemp * 10) / 10,
      dcPower: Math.round(dcPower * 100) / 100,
      acPower: Math.round(acPower * 100) / 100,
      efficiency: Math.round((acPower / (systemSize * irradiance / 1000 || 1)) * 1000) / 10,
      losses: {
        temperature: Math.round(tempLoss * 1000) / 10,
        soiling: Math.round(soilingLoss * 1000) / 10,
        shading: Math.round(shadingLoss * 1000) / 10,
        mismatch: Math.round(mismatchLoss * 1000) / 10,
        wiring: Math.round(wiringLoss * 1000) / 10,
        inverter: Math.round(inverterLoss * 1000) / 10,
      },
    });
  }

  return results;
}

