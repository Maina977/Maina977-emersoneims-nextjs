export function calculateElectricalDesign(
  panelWattage: number,
  panelVoc: number,
  panelVmpp: number,
  panelIsc: number,
  panelImpp: number,
  inverterVmppRange: { min: number; max: number },
  inverterMaxVoc: number,
  inverterMaxIsc: number,
  cableLength: number,
  ambientTemp: { min: number; max: number }
): ElectricalDesign {
  // Temperature corrected Voc (worst case at -10°C)
  const tempCoef = -0.0029; // Typical for mono panels
  const vocCorrected = panelVoc * (1 + tempCoef * (ambientTemp.min - 25));

  // Calculate max panels per string
  const maxPanelsPerString = Math.floor(inverterMaxVoc / vocCorrected);

  // Calculate min panels per string (at max temp)
  const vmppHot = panelVmpp * (1 + tempCoef * (ambientTemp.max - 25));
  const minPanelsPerString = Math.ceil(inverterVmppRange.min / vmppHot);

  // Optimal panels per string
  const panelsPerString = Math.min(maxPanelsPerString, Math.max(minPanelsPerString, 12));

  // Cable sizing (IEC 60364)
  const current = panelIsc * 1.25;
  const cableResistivity = 0.0175; // Copper at 20°C
  const maxVoltageDrop = 0.03; // 3%
  const voltage = panelVmpp * panelsPerString;
  const cableArea = (2 * cableLength * current * cableResistivity) / (maxVoltageDrop * voltage);
  const dcCableSize = Math.ceil(cableArea / 2.5) * 2.5; // Round to standard sizes

  // Protection sizing (NEC 690)
  const dcFuseRating = Math.ceil(panelIsc * 1.56 / 5) * 5;
  const dcIsolatorRating = Math.ceil(vocCorrected * panelsPerString * 1.2 / 100) * 100;

  return {
    stringConfiguration: {
      stringsPerMPPT: 2,
      panelsPerString,
      totalStrings: 2,
      vocMax: Math.round(vocCorrected * panelsPerString * 10) / 10,
      iscMax: Math.round(panelIsc * 2 * 10) / 10,
      vmppRange: {
        min: Math.round(vmppHot * panelsPerString),
        max: Math.round(panelVmpp * panelsPerString * 1.05),
      },
    },
    cabling: {
      dcCableSize: Math.max(4, dcCableSize),
      acCableSize: dcCableSize * 1.5,
      earthCableSize: Math.max(6, dcCableSize),
      voltageDrop: Math.round((2 * cableLength * current * cableResistivity / (dcCableSize * voltage)) * 1000) / 10,
      cableType: 'H1Z2Z2-K Solar Cable',
      conduitSize: Math.ceil(Math.sqrt(dcCableSize * 2) * 4),
    },
    protection: {
      dcFuseRating,
      dcIsolatorRating,
      acBreakerRating: Math.ceil(current * 1.25 / 5) * 5,
      spdType: 'Type 1+2 DC SPD',
      rccdRating: 30,
      earthFaultProtection: 'IMD (Insulation Monitoring Device)',
    },
    grounding: {
      electrodeType: 'Copper-bonded ground rod',
      electrodeDepth: 2.4,
      targetResistance: 5,
      bondingMethod: 'Exothermic welding',
      lightningProtection: true,
    },
  };
}

