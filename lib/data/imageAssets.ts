/**
 * Get image URL with fallback
 */
export function getImageUrl(
  category: keyof typeof imageAssets,
  key: string,
  fallback?: string
): string {
  const categoryImages = imageAssets[category] as Record<string, string>;
  return categoryImages[key] || fallback || imageAssets.fallbacks.placeholder;
}

/**
 * Get all images for a category
 */
export function getCategoryImages(category: keyof typeof imageAssets): Record<string, string> {
  return imageAssets[category] as Record<string, string>;
}

